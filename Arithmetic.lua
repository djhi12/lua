--[[

Write Lua code to perform the following arithmetic operations on two numbers, num1 and num2:

Calculate their sum.
Calculate their difference.
Calculate their product.
Calculate their quotient.
Print the results in a user-friendly format, including labels for each value.

Example:
If num1 is 10 and num2 is 5, the output should be:

Number 1: 10
Number 2: 5
Sum: 15
Difference: 5
Product: 50
Quotient: 2
]] -- Define variables
local num1 = 10
local num2 = 5

-- Perform arithmetic operations
local sum = num1 + num2
local difference = num1 - num2
local product = num1 * num2
local quotient = num1 / num2

-- Print the results
print("Number 1:", num1)
print("Number 2:", num2)
print("Sum:", sum)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
